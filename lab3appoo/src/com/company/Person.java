package com.company;

public class Person {
    private String name;
    private String surname;
    private int day;
    private int month;
    private int year;

    public String getName() {
        return name;
    }

    public void setName(String newName) {
        name = newName;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String newSurname) {
        surname = newSurname;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int newDay) {
        day = newDay;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int newMonth) {
        month = newMonth;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int newYear) {
        year = newYear;
    }

}